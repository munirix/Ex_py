'''
24. Faça um programa que calcule o mostre a média aritmética de N notas.
'''

n = int(input('Digite o número de notas: '))
soma = 0

for i in range(1, n + 1):
    nota = float(input('Entre com a %d nota: ' %i))
    soma =+ nota
print('A média das notas é %.2f' %(soma / n))
    
        
            
        




    





    


    



